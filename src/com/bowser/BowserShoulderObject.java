package com.bowser;

public class BowserShoulderObject {
	
	public Integer mode;
	public Integer speed;
	public Integer colorOne;
	public Integer colorTwo;
	public Integer colorThree;
	
	public  BowserShoulderObject() {
		
	}
	
	public BowserShoulderObject(Integer mode, Integer speed, Integer colorOne, Integer colorTwo, Integer colorThree) {
		this.mode = mode;
		this.speed = speed;
		this.colorOne = colorOne;
		this.colorTwo = colorTwo;
		this.colorThree = colorThree;
	}
	

}
